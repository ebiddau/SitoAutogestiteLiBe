	<h2>Connettere il PC di un relatore</h2>
		<ul>
			<li>Audio: attaccare il cavetto verde al pc del relatore</li><br>
			<li>Internet: attaccare il cavo di rete al pc del relatore oppure accesso Wi-Fi (da vedere con la direzione/sistemisti)</li><br>
			<li>Video: attaccare al pc del relatore il cavo video che è attaccato <u>al PC del liceo</u>, non staccare altro. L'immagine verrà visualizzata sullo schermo e sul beamer, è normale</li><br>
		</ul><br>

	<h2>"Non c'è audio!"</h2>
		<ul>
			<li>Le casse sono accese e collegate al PC in uso (cavetto verde)?</li><br>
			<li>Il volume delle casse, del PC e, per esempio, di youtube sono > 0?</li><br>
		</ul><br>

	<h2>"Non si vede nulla!"</h2>
		<ul>
			<li>Il beamer è acceso?</li><br>
			<li>Il PC del relatore/del liceo è collegato al cavo video?</li><br>
			<li>Controllare che il beamer non sia su "Hide"</li><br>
			<li>Premere il tasto <kbd>Win</kbd> + <kbd>P</kbd> e verificare che sia impostato su duplica</li>
		</ul><br>

	<h2>IMPORTANTE</h2>
		Alla fine dell'attività/delle autogestite, rimettere tutti i cavi come nell'immagine qui sopra e controllare che siano ben inseriti!
